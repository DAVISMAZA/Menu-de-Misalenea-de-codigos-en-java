public class imprimir_numerosdel_unoaldiez {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("Los números del 1 al 10 son:");
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println(); // Para una nueva línea al final
    }
}